public class Loops{
	public static void main(String args[])
	{
		
		for (int i=0;i<10;i++)
		{
			System.out.println("FUCK YOU MF");	//for loop   (this will be an infinity loop when there
														// wil be no condition)
		}
		int i=1;
		while (i<12)
		{
			System.out.println(i);		//while  loop
			i++;
		}
		int i=1;
		do
		{
			System.out.println("I love you");		//do while loop
			i++;
		}
		while(i<20);


	}
}